package conditionalStatements;

public class SwitchCase {

	public static void main(String[] args) {
		
		switch(30) {
		
		case 10:
			System.out.println("Case Value is 10");
			break;
			
		case 20:
			System.out.println("Case Value is 20");
			break;
			
		default:
		 System.out.println("Case Value is default");
		}

	}

}
